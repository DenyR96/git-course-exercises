#include <iostream>
using namespace std;

int main()
{
   int num1, num2;

   cout << "Insert first number: ";
   cin >> num1;

   cout << "Insert second number: ";
   cin >> num2;
   cout << endl;

   cout << "Sum: " << num1 + num2 << endl;
   cout << "Difference: " << num1 - num2 << endl;
   cout << "Multiplication: " << num1 * num2 << endl;
   cout << "Division: " << num1 / num2 << endl;
}
